    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        double telefono;
        double monto;
        try{
        String operadora="";
        monto=Double.parseDouble(txtMonto.getText());
        telefono=Double.parseDouble(txtTelefono.getText());
        if(rbtClaro.isSelected()){
            operadora="Claro";
        }
        if(rbtMovistar.isSelected()){
            operadora="Movistar";
        }
        if(rbtTuenti.isSelected()){
            operadora="Tuenti";
        }
        Recarga recarga=new Recarga (telefono,monto,operadora);
        monto=cuentas.getCapital();
        cuentas.setCapital(monto-recarga.getMonto());
        JOptionPane.showMessageDialog(rootPane,"La recarga se ha realizado exitosamente");
        limpiarGUI();
        }catch(NumberFormatException exception){
            JOptionPane.showMessageDialog(rootPane,"Un campo fue ingresado incorrectamente");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

